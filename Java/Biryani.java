import java.util.Scanner;
class Codechef
{
	    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int t = input.nextInt();
        while(t!=0)
        {
            int x= input.nextInt();
            int y= input.nextInt();
            System.out.println(x*y);
            t--;
        }
    }
    
}
